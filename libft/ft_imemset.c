#include "libft.h"

void	*ft_imemset(void *s, int c, size_t n)
{
	int				*tmp;
	int				a;

	tmp = s;
	a = 0;
	while (n != 0)
	{
		tmp[a] = c;
		n--;
		a++;
	}
	return (s);
}
